package org.skm.RemoteControlAPI;

public class GarageDoor {
    public void up(){
        System.out.println("door going up");
    }

    public void down(){
        System.out.println("door going down");
    }
    public void stop(){
        System.out.println("stop door");
    }
    public void lightOn(){
        System.out.println("garage door on");
    }
    public void lightOff(){
        System.out.println("garage door off");
    }
}
